'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import type { ConnectionStatus } from '@/types/telemetry';

interface SidebarProps {
  status: ConnectionStatus;
}

const navItems = [
  { href: '/', label: 'Overview', icon: HomeIcon },
  { href: '/cpu', label: 'CPU', icon: CpuIcon },
  { href: '/ram', label: 'RAM', icon: RamIcon },
  { href: '/storage', label: 'Storage', icon: StorageIcon },
  { href: '/network', label: 'Network', icon: NetworkIcon },
  { href: '/system', label: 'System', icon: SystemIcon },
];

export default function Sidebar({ status }: SidebarProps) {
  const pathname = usePathname();

  return (
    <aside className="sidebar">
      {/* Logo */}
      <div className="sidebar-logo">
        <div className="sidebar-logo-icon">
          <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <rect x="2" y="3" width="20" height="14" rx="2" ry="2" />
            <line x1="8" y1="21" x2="16" y2="21" />
            <line x1="12" y1="17" x2="12" y2="21" />
          </svg>
        </div>
        <span className="sidebar-logo-text">SysWatch</span>
      </div>

      {/* Navigation */}
      <nav className="sidebar-nav">
        {navItems.map((item) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`sidebar-nav-item ${isActive ? 'active' : ''}`}
            >
              <item.icon />
              <span>{item.label}</span>
            </Link>
          );
        })}
      </nav>

      {/* Connection Status */}
      <div className="sidebar-footer">
        <div className={`connection-badge ${status}`}>
          <span className="connection-dot" />
          <span className="connection-label">
            {status === 'connected' ? 'Connected' : status === 'connecting' ? 'Connecting...' : 'Disconnected'}
          </span>
        </div>
      </div>
    </aside>
  );
}

